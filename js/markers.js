// Export — ícones e função de escolha
// =============================================================

// Dicionário
// Não exportado diretamente; consumidores devem usar getIcone().
const iconesPorChave = {
  'capital': iconeCapital,
  'cidade-estado': iconeCidadeEstado,
  'povoado': iconePovoado,
  'hidrografia': iconeHidrografia,
  'terreno': iconeTerreno,
  'rota': iconeRota,
};

// Escolhe o ícone correto pra um marker.
// Centraliza a lógica de "sub-categorias visuais" (ex: capital é
// uma cidade-estado com ícone diferente).
window.getIcone = function(marker) {
  if (marker.categoria === 'cidade-estado' && marker.capital) {
    return iconesPorChave['capital'];
  }
  return iconesPorChave[marker.categoria];
};

